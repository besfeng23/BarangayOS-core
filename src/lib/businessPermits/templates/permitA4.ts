type PermitPrintData = {
  barangayName: string;
  municipality: string;
  province: string;

  businessName: string;
  ownerName: string;
  addressText: string;
  category?: string;

  year: number;
  feeAmount: number;
  orNo?: string;
  issuedAtText: string;
  controlNo: string;

  captainName: string;
  secretaryName: string;
};

